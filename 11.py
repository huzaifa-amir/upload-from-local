import turtle as t
t.pencolor("#ECE80E")
t.bgcolor('blue')
t.pensize(1)
t.speed(0)
t.fillcolor('red')    
t.begin_fill()
t.shape('square')
for i in range(1000):
    t.fd(1+i)
    t.left(72)
    t.backward(66)
    t.title('head')
    t.circle(44)
    t.clone
    t.degrees(44)
    t.distance(3,33)
t.done()